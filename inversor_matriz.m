function [inversa] = inversor_matriz(a,b)

    inversa = zeros(3,1);

    inversa(1,1) = (a(2,2)*a(3,3)-a(2,3)*a(3,2))*b(1,1) + (a(1,3)*a(3,2)-a(1,2)*a(3,3))*b(2,1) + (a(1,2)*a(2,3)-a(1,3)*a(2,2))*b(3,1);
    inversa(2,1) = (a(2,3)*a(3,1)-a(2,1)*a(3,3))*b(1,1) + (a(1,1)*a(3,3)-a(1,3)*a(3,1))*b(2,1) + (a(1,3)*a(2,1)-a(1,1)*a(2,3))*b(3,1);
    inversa(3,1) = (a(2,1)*a(3,2)-a(2,2)*a(3,1))*b(1,1) + (a(1,2)*a(3,1)-a(1,1)*a(3,2))*b(2,1) + (a(1,1)*a(2,2)-a(1,2)*a(2,1))*b(3,1);
end